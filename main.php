	<h3>量子資訊與網路安全實驗室（原：資訊安全實驗室）</h3>
	<h4>Quantum Information and Network Security Laboratory</h4>
	<br />
		
	<b>實驗室簡介</b>
	<hr />	
	<div style="text-indent: 26px;">
		歡迎光臨『量子資訊與網路安全實驗室』。本實驗室成立至今已二十餘年，專攻資訊安全（包括網路安全、雲端技術安全、浮水印、多媒體安全、資訊隱藏、密碼學RFID安全、智慧卡安全、門禁系統及錯誤控制碼之研究），曾發表台灣第一套網路安全系統及檔案保護系統（Don't Worry Secure System）供外界使用。最近十年，我們開始探討量子通訊安全，並有豐碩的成果發表於國際期刊上。非常歡迎新同學加入。<br/><br/>
	</div>
	<b>歡迎加入</b>
	<hr />	
	<div style="text-indent: 26px;">
	歡迎對資訊安全有興趣的新同學加入，我們會引導沒有基礎的同學入門。<br/>
	同學可先至實驗室了解情況，我們的實驗室在成功校區資訊系65603。<br/>
	名額有限，欲進從速。
	</div>
